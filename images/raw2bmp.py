def write_bmp_8bit_mode13(filename, grid):
    if len(grid) != HEIGHT or any(len(row) != WIDTH for row in grid):
        raise ValueError("Grid must be 24x24")

    # BMP is bottom-up
    flat = []
    for y in range(HEIGHT - 1, -1, -1):
        flat.extend(grid[y])

    row_size = WIDTH
    padding = (4 - (row_size % 4)) % 4
    pixel_array_size = (row_size + padding) * HEIGHT
    file_size = 54 + 1024 + pixel_array_size

    # --- BMP HEADER ---
    bmp_header = struct.pack("<2sIHHI", b"BM", file_size, 0, 0, 54 + 1024)

    # --- DIB HEADER ---
    dib_header = struct.pack(
        "<IIIHHIIIIII",
        40,         # header size
        WIDTH,
        HEIGHT,
        1,
        8,          # 8-bit indexed
        0,
        pixel_array_size,
        2835,
        2835,
        256,
        0
    )

    # --- VGA PALETTE (Mode 13h compatible) ---
    palette = vga_palette()

    palette_data = bytearray()
    for r, g, b in palette:
        palette_data.extend(struct.pack("<BBBB", b, g, r, 0))  # BMP uses BGR0

    # --- PIXEL DATA ---
    pixel_data = bytearray()
    idx = 0

    for _ in range(HEIGHT):
        for _ in range(WIDTH):
            pixel_data.append(flat[idx])
            idx += 1

        pixel_data.extend(b"\x00" * padding)

    # --- WRITE FILE ---
    with open(filename, "wb") as f:
        f.write(bmp_header)
        f.write(dib_header)
        f.write(palette_data)
        f.write(pixel_data)


def load_raw_24x24(path):
    with open(path, "rb") as f:
        data = f.read()

    if len(data) != SIZE:
        raise ValueError(f"Expected {SIZE} bytes, got {len(data)}")

    return [list(data[i*WIDTH:(i+1)*WIDTH]) for i in range(HEIGHT)]


if __name__ == "__main__":
    import sys

    if len(sys.argv) != 3:
        print("Usage: python convert.py input.bin output.bmp")
        raise SystemExit(1)

    inp, outp = sys.argv[1], sys.argv[2]

    grid = load_raw_24x24(inp)
    write_bmp_8bit_mode13(outp, grid)

    print(f"Mode 13h BMP written: {outp}")
